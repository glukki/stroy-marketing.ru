<h1>17-ый МЕЖДУНАРОДНЫЙ ПРАКТИЧЕСКИЙ СЕМИНАР "ТОРГОВЛЯ ПРИРОДНЫМ КАМНЕМ И ЕГО ГЛУБОКАЯ ПЕРЕРАБОТКА"</h1>
<h2>(добыча, обработка, глубокая переработка, продукция, оборудование, финансирование)</h2>

<p>В наступивший послекризисный период экономической и финансовой стабильности объем рынка продукции из природного камня растет вслед за ростом объемов строительства и ремонта. По прогнозам, в 2011 году как минимум будет обеспечен рост ВВП в размере 3-4%, хотя оптимисты верят в больший рост объемов по сравнению с 2010 годом. При этом в 2011 году ожидается скачкообразный рост задела в строительстве и, соответственно, потребности в строительных материалах и изделиях. Поэтому особенно актуальным становится расширение деловых контактов.</p>

<p>Фирма "Строй-Маркетинг" приглашает Вас принять участие в проводящемся, в г.Санкт-Петербурге 14-15 ноября 2011 года 17-ом Международном практическом семинаре "Торговля природным камнем и его глубокая переработка".</p>
<p>Основная цель проводящегося в 2011 году семинара - установление новых контактов между производителями продукции  и их потребителями, ознакомление с современными технологиями добычи, обработки и глубокой переработки природного камня.</p>
<p>На семинаре будут представлены 6 направлений деятельности, связанной с развитием отрасли:</p>
<ul>
<li>	Оборудование для добычи природного камня.</li>
<li>	Оборудование для обработки и дробления природного камня (слэбы, плиты, брусчатка, щебень и т.д.);</li>
<li>	Оборудование для производства минералватного утеплителя и базальто-пластиковых труб из отходов добычи камня;</li>
<li>	Торговля природным камнем и изделиями из него (блоки, плиты, брусчатки, щебень,  минералватный утеплитель, базальто-пластиковые трубы и пр.).</li>
<li>Купля-продажа предприятий с лицензиями на добычу природного камня.</li>	
<li>Инвестиции и кредиты;</li>	
</ul>
<p>Участие в семинаре позволит:</p>
<ul>
<li>     увеличить объем продаж оборудования и изделий из природного камня; </li>
<li>  установить новые профессиональные контакты и укрепить существующие деловые отношения; </li> 
<li>   увеличить вероятность выбора Покупателями именно Вашей продукции; </li>
<li>  выявить новые возможности для бизнеса.</li> 
</ul>
