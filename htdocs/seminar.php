<h2>16-ЫЙ МЕЖДУНАРОДНЫЙ ПРАКТИЧЕСКИЙ СЕМИНАР "ТОРГОВЛЯ ПРИРОДНЫМ КАМНЕМ"</h2>
<p><em><strong>(добыча, обработка, глубокая переработка, оборудование, финансирование)</strong></em></p>
<p>
&nbsp;
</p>
<p>В существующих финансовых и экономических условиях страны объем рынка продукции из природного камня может вырасти только вслед за ростом объемов строительства и ремонта. По прогнозам, в 2010 году как минимум будет обеспечен нулевой рост, хотя оптимисты верят в 10-15% роста объемов строительства по сравнению с 2009 годом. Но уже в 2011 году ожидается скачкообразный рост задела в строительстве и, соответственно, потребности в строительных материалах и изделиях.</p>

<p>Фирма "Строй-Маркетинг" в период 15-16 ноября 2010 года проводит  16-ый Международный практический семинар "Торговля природным камнем" и его глубокая переработка", который состоится в Санкт-Петербурге, и приглашает Вас принять участие в нём.</p>
<p>Основная цель проводящегося в 2010 году семинара - установление новых контактов между производителями продукции и услуг и их потребителями, ознакомление с современными технологиями добычи, обработки и глубокой переработки природного камня.</p>
<p>На семинаре будут представлены 6 направлений деятельности, связанной с развитием отрасли:</p>
<UL>
<li>Оборудование для добычи природного камня.</li>
<li>Оборудование для обработки и дроблении природного камня;</li>
<li>Оборудование для производства минералватного утеплителя и базальто-пластиковых труб из отходов добычи камня;</li>
<li>Инвестиции и кредиты;</li>
<li>Торговля природным камнем и изделиями из него (блоки, плиты, брусчатки, щебень,  минералватный утеплитель, базальто-пластиковые трубы и пр.).</li>
<li>Купля-продажа предприятий с лицензиями на добычу природного камня</li>
</UL>

<p>Участие в семинаре позволит:</p>
<ul>
<li>увеличить объем продаж оборудования и изделий из природного камня, а также контролируемую долю рынка в России и за ее пределами;</li>
<li>ознакомиться с передовыми технологиями и техникой; </li>
<li>установить новые профессиональные контакты и укрепить существующие деловые отношения; </li>
<li>увеличить вероятность выбора Покупателями именно Вашей продукции;</li> 
<li>выявить новые возможности для бизнеса.</li>
</ul>
<p>Место проведения семинара - Отель "Ренессанс. Санкт-Петербург. Балтик отель" (улица Почтамтская, дом 4), зал "Бальный"- около 50 метров от Исаакиевской площади.</p>

