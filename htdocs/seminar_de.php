<ul>
<li>- Investitionen und Kredite f&uuml;r die Entwicklung der Branche;</li>
<li>- Handel mit Naturstein und Gesteinserzeugnissen;</li>
<li>- Maschinen und Ausr&uuml;stungen zur Gewinnung von Naturstein;</li>
<li>- Maschinen und Anlagen zur Bearbeitung und Verarbeitung von Naturstein;</li>
<li>- Verwendung von Naturstein bei Fasenbekleidung, innerem Ausbau, Kaibekleidung usw.</li>
</ul>

<p>&nbsp;</p>

<p>Die Teilnahme am Seminar erlaubt es:</p>
<ul>
