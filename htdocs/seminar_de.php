<p>Um diesen Bedarf an Baustoffen zu decken, ist es notwendig, einheimische Produktion auf Basis moderner Ausr&uuml;stungen, Maschinen/Anlagen mid neuer Techinologien zu entwickeln als auch internationalen Natursteinhandel weiter zu fordern.
