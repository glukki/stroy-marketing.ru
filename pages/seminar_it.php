<h2>ХVI SEMINARIO PRATICO INTERNAZIONALE</h2>
<h2>"COMMERCIO DELLA PIETRA NATURALE"</h2>
<p><strong><em>(blocchi,piastre,articoli, pietrisco, attrezzatura, investimenti)</em></strong></p>

<p>Egregio Sig. Presidente,</p>
<p>ASSOCIAZIONE “LA PIETRA NATURALE NAZIONALE” e la ditta "STROY MARKETING" LA cortesamente invitano a prendere partecipazione al XVI0 seminario internazionale "COMERCIO DELLA PIETRA NATURALE"che si terra’ a San Pietroburgo dal  16 al 18 Marzo 2009.</p>

<p>&nbsp;</p>

<p>La ristrutturazione e costruzione dei diversi edifici, lungofiumi, della ferrovia di alta velocita' Mosca - San Pietroburgo di lunghezza piu di 600 chilometri, la costruzione   delle autostrade circolari a Mosca ed a San Pietroburgo, la costruzione dei porti di mare nella provincia di Leningrado, rocostruzione e costruzione di diversi oggetti hanno aumentato notevolmente fabbisogni della Russia nei materiali della pietra naturale (blocchi, piastre, mattonelle ect.). Si stanno svolgendo i lavori enormi di restauro del centro storico della citta' con tanto consumo della pietra naturale.Per poter soddisfare questi fabbisogni ci vuole un sviluppo della produzione nazionale sulla base dell'acquisto dell'attrezzatura moderna ed inserimento delle tecnologie avanzate,investimenti ed anche I'aumento del commercio internazionale degli articoli di pietra naturale.</p>

<p>&nbsp;</p>

<p>Nel 2007-2008 per acquistare t'attezzatura per escavazione ed elaborazione della pietra naturale nella provincia di Leningrado ed in Carelia venivano spesi piu di 40 milioni USD. Pero, le spese ancora di piu ci vorranno nel 2009-2012 per adempire i programmi progettati.</p>

<p>&nbsp;</p>

<p>Lo scopo principale del seminario che si terra' nel 2009 e'quanto segue: lo sviluppo delta
collaborazione d'affari con la stipulazione dei contratti per la compravendita dei blocchi di pietra, degli articoli di pierta naturale, dell'attrezzatura per l'escavazione e l'estrazione della pietra, dei mezzi di   trasporto, degli utensili per l'esecuzione dei lavori di rivestimento, gli incontri tra gli eventuali partners, e le ditte di investimenti   per realizzare i progetti tecnico-economici nel comune reciproco interesse.</p>

<p>Nei  corso  del  seminario  verranno  organizzate  6  direzioni  dell'attivita'  concernenti  la produzione e commercio degli articoli di pietra naturale:</p>

<p>-	Investimenti e crediti per lo sviluppo del settore;</p>
<p>-	commercio della pietra naturale e degli articoli di pietra;</p>
<p>-	attrezzatura per l'escavazione della pietra naturale;</p>
<p>-	attrezzatura per l'elaborazione della pietra naturale;</p>
<p>-	costruzione e lavori di costruzione stradali con l'uso della pietra naturale;</p>
<p>-	buon mantenimento e ristrutturazione delle citta’;</p>
<p>-	compravendita delle aziende con una licenza per escavazione della pietra naturale.</p>

<p>-	Partecipazione al seminario consentira':</p>
<p>-	stabilire i contatti professionali nuovi e rafforzare i contatti d'affari esistenti;</p>
<p>-	aumentare il volume delle vendite dell'attrezzatura e degli acquisti degli articoli di pietra
naturale (biocchi,piastre,mattonelle,bramme ect);</p>
-	aumentare una parte controllata del mercato in Russia e nei paesi CSI;
-	verificare delle possibilita' nuove per busines;


