<h2>XVI  INTERNATIONAL  PRACTICAL  SEMINAR 'NATURAL  STONE  TRADE'</h2>
<p><em>(quarrying, processing, equipment, trade, construction, restoration, credits)</em></p>

<p>The Native Natural Stone Association and Stroy-Marketing. are pleased to invite you to participate in the <strong>16th International Practical Seminar on Natural Stone Trade</strong>, which will be held in St. Petersburg (Russia) from 16 to 18 March 2009.</p>

<p>&nbsp;</p>

<p>Reconstruction and construction of different buildings, embankments, express railroad Moscow - St. Petersburg with the total length of more than 600 km, the building of ring roads around Moscow and St. Petersburg and sea ports in the Region of Leningrad, as well as other large construction and reconstruction projects have raised the need for natural stone materials and products (slabs, units, paving stone, crushed stone, etc.). In order to meet the growing demand for these materials it is necessary to develop their production in Russia by introducing modern technologies and purchasing up-to-date equipment as well as boost the sales of stone products.</p>

<p>&nbsp;</p>


<p>In 2007-2008 more than euro 40 mln were spent on purchasing equipment for natural stone quarrying and processing only in Karelia and Leningrad Region. But even larger sums of money are to be expended in 2009 - 2012 on realizing relevant projects.</p>

<p>&nbsp;</p>


<p>The seminar programme will be based on the following five aspects of activities connected with the development of stone materials production and trade:</p>

<ul>
	<li>investments</li>
	<li>natural stone and stone products trade</li>
	<li>equipment for stone quarrying</li>
	<li>equipment for natural stone processing</li>
	<li>the use of natural stone in construction  and reconstruction (claddings of buildings and embankments, etc.)</li>
</ul>

<p>&nbsp;</p>

<p>The seminar provides a perfect opportunity for the participants:</p>
<ul>
	<li>to establish new business contacts and strengthen the existing ones;</li>
	<li>to increase the volume of equipment sales and natural stone purchases (including stone units, slabs, paving stone, etc.);</li>
	<li>to increase the controlled share of the market in Russia and CIS;</li>
	<li>to convince potential buyers of the advantages of your own products;</li>
	<li>to discover new openings for business.</li>
</ul>

